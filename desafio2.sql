SELECT JOB_TITLE AS 'Cargo',
CASE
	WHEN AVG(MIN_SALARY + MAX_SALARY) BETWEEN 5000 AND 10000 THEN 'Baixo'
    WHEN AVG(MIN_SALARY + MAX_SALARY) BETWEEN 10001 AND 20000 THEN 'Médio'
	WHEN AVG(MIN_SALARY + MAX_SALARY) BETWEEN 20001 AND 30000 THEN 'Alto'
	WHEN AVG(MIN_SALARY + MAX_SALARY) > 30001 THEN 'Altíssimo'
END AS 'Nível'
FROM hr.jobs
GROUP BY JOB_TITLE
ORDER BY JOB_TITLE ASC;
