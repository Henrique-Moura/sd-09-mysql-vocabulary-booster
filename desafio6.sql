SELECT 
    CONCAT(EMPLOY.FIRST_NAME, ' ', EMPLOY.LAST_NAME) AS 'Nome completo',
    JOBS.JOB_TITLE AS 'Cargo',
    HISTORY.START_DATE AS 'Data de in√≠cio do cargo',
    DEPARTS.DEPARTMENT_NAME AS 'Departamento'
FROM
    hr.employees AS EMPLOY
        INNER JOIN
    hr.job_history AS HISTORY ON HISTORY.EMPLOYEE_ID = EMPLOY.EMPLOYEE_ID
        INNER JOIN
    hr.jobs AS JOBS ON JOBS.JOB_ID = HISTORY.JOB_ID
        INNER JOIN
    hr.departments AS DEPARTS ON DEPARTS.DEPARTMENT_ID = HISTORY.DEPARTMENT_ID
ORDER BY CONCAT(FIRST_NAME, ' ', LAST_NAME) DESC , JOBS.JOB_TITLE;
