SELECT
  j.JOB_TITLE AS 'Cargo',
  ROUND(AVG(e.SALARY), 2) AS 'Média salarial',
  CASE
    WHEN ROUND(AVG(e.SALARY), 2) BETWEEN 2000.0 AND 5800.0 THEN 'Júnior'
    WHEN ROUND(AVG(e.SALARY), 2) BETWEEN 5801.0 AND 7500.0 THEN 'Pleno'
    WHEN ROUND(AVG(e.SALARY), 2) BETWEEN 7501.0 AND 10500.0 THEN 'Sênior'
    ELSE 'CEO'
  END
  AS 'Senioridade'
FROM employees e INNER JOIN jobs j ON e.JOB_ID = j.JOB_ID 
GROUP BY j.JOB_TITLE
ORDER BY ROUND(AVG(e.SALARY), 2) ASC, j.JOB_TITLE;
