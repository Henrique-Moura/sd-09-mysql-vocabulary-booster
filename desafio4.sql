SELECT t2.JOB_TITLE AS Cargo, 
ROUND(AVG(SALARY), 2) AS 'Média Salarial', 
CASE
WHEN ROUND(AVG(SALARY), 2) < 5801 THEN 'Júnior'
WHEN ROUND(AVG(SALARY), 2) BETWEEN 5801 AND 7500 THEN 'Pleno'
WHEN ROUND(AVG(SALARY), 2) BETWEEN 7501 AND 10500 THEN 'Sênior'
WHEN ROUND(AVG(SALARY), 2) > 10500 THEN 'CEO'
ELSE ''
END AS 'Senioridade'
FROM employees AS t1
INNER JOIN jobs AS t2
ON t1.JOB_ID = t2.JOB_ID
GROUP BY Cargo ORDER BY ROUND(AVG(SALARY), 2), Cargo;
